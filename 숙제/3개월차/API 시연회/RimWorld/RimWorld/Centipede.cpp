CCentipede::CCentipede()
{
}

CCentipede::~CCentipede()
{
	Release();
}

void CCentipede::Initialize()
{
    CPawn::Initialize();

    m_tInfo.fCX = 64.f;
    m_tInfo.fCY = 64.f;

    m_fSpeed = 1.f;

    m_eRenderID = RENDER_GAMEOBJECT;

    //무기 생성
    m_pRangedWeapon = CAbstractFactory<CChargeBlasterLight>::Create(m_tInfo.fX, m_tInfo.fY);
    CObjMgr::Get_Instance()->Add_Object(OBJ_WEAPON, m_pRangedWeapon);
    m_pRangedWeapon->Set_Target(this);

    Take_Damage(50.f);
}

int CCentipede::Update()
{
    if (m_bDestroyed)
        return OBJ_DESTROYED;

    //죽었으면 리턴
    if (m_bDead)
    {
        return OBJ_NOEVENT;
    }

    //타겟 있으면 따라가기
    if (m_pTarget)
    {
        //타겟이 죽으면 타겟 없애기
        if (CPawn* pPawnTarget = dynamic_cast<CPawn*>(m_pTarget))//타겟이 Pawn이고 죽었으면
        {
            if (pPawnTarget->Get_IsDead())
            {
                Set_Target(nullptr);
                return OBJ_NOEVENT;
            }
       
        }
        else if (CSteelWall* pWallTarget = dynamic_cast<CSteelWall*>(m_pTarget))//타겟이 Wall이고 부서졌으면
        {
            if (pWallTarget->Get_IsBrokenDown())
            {
                Set_Target(nullptr);
                return OBJ_NOEVENT;
            }
        }
        
        
        //멈춰서 공격 중일 때 못찾게해야함!!!!!!!!!!!
        Move_To(POS{ m_pTarget->Get_Info().fX, m_pTarget->Get_Info().fY });
        
    }

    if (m_bNavigating)
    {
        Navigate();
    }


    __super::Update_Rect();

    return OBJ_NOEVENT;
}

void CCentipede::Late_Update()
{
    //죽었으면 리턴
    if (m_bDead)
    {
        return;
    }

    Calculate_MoveDir();
    Measure_Target();

    //사정 거리내에 있고 본인이 죽지 않았다면
    if (IsWithinRange()&&!m_bDead)
    {
        m_bAttack = true;
    }
    else
    {
        m_bAttack = false;
    }

    //FindTarget();
    //타겟이 없을 경우
    //림 오브젝트리스트에서 가장 가까운 림을 찾고 타겟으로 설정
    if (!m_pTarget)
    {
        Find_Target();
    }

    //마우스 클릭 했을 때 타겟으로 설정
    POINT	ptMouse{};

    GetCursorPos(&ptMouse);
    ScreenToClient(g_hWnd, &ptMouse);

    int		iScrollX = (int)CScrollMgr::Get_Instance()->Get_ScrollX();
    int		iScrollY = (int)CScrollMgr::Get_Instance()->Get_ScrollY();

    ptMouse.x -= iScrollX;
    ptMouse.y -= iScrollY;

    if (PtInRect(&m_tRect, ptMouse))
    {
        //우클릭은 타겟의 공격 타겟으로 설정
        if (CKeyMgr::Get_Instance()->Key_Up(VK_RBUTTON))
        {
            if (CObj* pTarget = CColonyMgr::Get_Instance()->Get_Target())
            {
                pTarget->Set_Target(this);
            }
            return;
        }
    }
    
}

